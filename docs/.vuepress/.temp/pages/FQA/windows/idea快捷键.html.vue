<template><h1 id="idea快捷键" tabindex="-1"><a class="header-anchor" href="#idea快捷键" aria-hidden="true">#</a> idea快捷键</h1>
<ul>
<li>Ctrl + A ，全选当前文件所有内容</li>
<li>Ctrl + B ，进入光标所在的方法的定义处，等效于 <code v-pre>Ctrl + 左键</code></li>
<li>Ctrl + C ，复制光标所在行或复制选择内容</li>
<li>Ctrl + D ，复制当前行到下一行</li>
<li>Ctrl + E ，显示最近打开的文件记录列表</li>
<li>Ctrl + F ，在当前文件进行文本查找</li>
<li>Ctrl + G ，在当前文件跳转到指定行处</li>
<li>Ctrl + H ，显示当前类的层次结构</li>
<li>Ctrl + I ，选择可以实现的方法（一般实现抽象方法时使用）</li>
<li>Ctrl + J ，插入自定义动态代码模板</li>
<li>Ctrl + K ，版本控制提交项目（项目加入版本控制时才能用）</li>
<li>Ctrl + L ，当前文件查找时，显示下一个的位置（关闭搜索框时也可以使用）</li>
<li>Ctrl + M ，跳转到光标所在位置</li>
<li>Ctrl + N ，默认根据类名查找文件（在搜索框上面可以选择其他类别）</li>
<li>Ctrl + O ，选择需要实现或者重写的方法</li>
<li>Ctrl + P ，方法参数提示显示</li>
<li>Ctrl + Q ，显示光标所在的变量 / 类名 / 方法名的文档说明内容</li>
<li>Ctrl + R ，替换文本</li>
<li>Ctrl + S ，保存当前文件</li>
<li>Ctrl + T ，版本控制更新项目，需要此项目有加入到版本控制才可用</li>
<li>Ctrl + U ，前往当前光标所在的方法的父类的方法 / 接口定义</li>
<li>Ctrl + V ，粘贴复制内容</li>
<li>Ctrl + W ，递进式选择代码块</li>
<li>Ctrl + X ，剪贴光标所在行或者所选内容</li>
<li>Ctrl + Y  ，删除光标所在行 或 删除选中的行</li>
<li>Ctrl + Z ，撤销更改内容</li>
<li>Ctrl + + ，展开代码</li>
<li>Ctrl + - ，折叠代码</li>
<li>Ctrl + / ，注释光标所在行代码（会根据当前不同文件类型使用不同的注释符号）</li>
<li>Ctrl + [ ，移动光标到当前所在代码的花括号开始位置</li>
<li>Ctrl + ] ，移动光标到当前所在代码的花括号结束位置</li>
<li>Ctrl + F3 ，调转到所选中的词的下一个引用位置</li>
<li>Ctrl + F4 ，关闭当前编辑文件</li>
<li>Ctrl + F8 ，设置光标当前行为断点，如果当前已经是断点则去掉断点</li>
<li>Ctrl + F9 ，build project</li>
<li>Ctrl + F11 ，光标、选中文件/文件夹处添加书签</li>
<li>Ctrl + F12 ，显示当前文件的结构，所有方法等</li>
<li>Ctrl + 数字(1,2,3…9) ，跳转到对应编号书签所在位置</li>
<li>Ctrl + Home ，跳转到文件头部</li>
<li>Ctrl + End ，跳转到文件尾部</li>
<li>Ctrl + Space ，基础代码补全，默认在 Windows 系统上被输入法占用，建议修改为 <code v-pre>Ctrl + 逗号</code></li>
<li>Ctrl + Delete ，删除光标后面的单词或是中文句</li>
<li>Ctrl + BackSpace ，删除光标前面的单词或是中文句</li>
<li>Ctrl + 左方向键 ，光标跳转到当前单词 / 中文句的左侧开头位置</li>
<li>Ctrl + 右方向键 ，光标跳转到当前单词 / 中文句的右侧开头位置</li>
<li>Ctrl + 上方向键 ，光标不动，文件内容往下移动</li>
<li>Ctrl + 下方向键 ，光标不动，文件内容往上移动</li>
<li>Ctrl+Shift+E，最近更改的文件</li>
<li>Ctrl+Alt+T，可以把代码包在一个块内，例如：try/catch</li>
<li>Ctrl+Alt+L，格式化代码</li>
<li>Ctrl+Alt+I，将选中的代码进行自动缩进编排，这个功能在编辑 JSP 文件时也可以工作</li>
<li>Ctrl+Alt+O，优化导入的类和包</li>
<li>shift+F6 ，重命名变量名</li>
</ul>
</template>
